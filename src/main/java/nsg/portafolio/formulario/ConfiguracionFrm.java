    private void jComboBoxConfiguracionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxConfiguracionesActionPerformed
        // TODO add your handling code here:

        Configuracion seleccion = (Configuracion) jComboBoxConfiguraciones.getSelectedItem();
        if (seleccion != null) {
            log.info("ID: " + seleccion.getConfiguracion_id() + ", Proyecto: " + seleccion.getNombre_proyecto());

            if (seleccion.getConfiguracion_id() == null) {
                setearCampos();
            } else {
                jTextFieldProyecto.setText(seleccion.getNombre_proyecto());

                jTextFieldPom.setText(seleccion.getPomDir());
                jTextFieldBuild.setText(seleccion.getBuildDir());
                jTextFieldWar.setText(seleccion.getWarName());
                jTextFieldWildfly.setText(seleccion.getWildflyDeployDir());
                jTextFieldMaven.setText(seleccion.getMavenExecutable());

            }
        }
    }//GEN-LAST:event_jComboBoxConfiguracionesActionPerformed

