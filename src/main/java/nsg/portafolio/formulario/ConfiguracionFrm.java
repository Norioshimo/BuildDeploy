    private void setearCampos() {
        log.info("Setear Campos");
        jTextFieldProyecto.setText("");
        jTextFieldPom.setText("");
        jTextFieldBuild.setText("");
        jTextFieldWar.setText("");
        jTextFieldWildfly.setText("");
        jTextFieldMaven.setText("");

    }

    private void actualizarLista() {
        log.info("Actualizar la lista de configuracion");
        // Crear lista de configuaraciones
        ConfiguracionDAO cDao = new ConfiguracionDAO();
        try {
            List<Configuracion> lista = cDao.listarTodas();

            log.info("Cantiad de Configuraciones " + lista.size());

            jComboBoxConfiguraciones.removeAllItems();

            jComboBoxConfiguraciones.addItem(Configuracion.builder().nombre_proyecto("Nuevo").build());

            lista.forEach(item -> {
                jComboBoxConfiguraciones.addItem(item);
            });

        } catch (Exception ex) {
            ex.printStackTrace();
            log.info("Error buscar lista de configuarciones. " + ex);
            JOptionPane.showMessageDialog(null, "Error buscar lista de configuarciones. " + ex,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

